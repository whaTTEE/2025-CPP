#include <iostream>
#include <bitset>

using namespace std;

int main()
{
    int a = 13;
    int b = 27;
    int c = a | b; // 비트 and 연산

    cout << bitset<8>(a) << endl;
    cout << bitset<8>(b) << endl;
    cout << bitset<8>(c) << endl;

    return 0;
}

/* Or '|' 출력값
00001101
00011011
00011111
*/
